export function findUser(username, password) {
  return mockUsers.find(u => u.username === username && u.password === password) || null
}

export function findUserByUsername(username) {
  return mockUsers.find(u => u.username === username) || null
}

export function getUserRef(userId) {
  const u = mockUsers.find(u => u.id === userId)
  if (!u) return null
  return { id: u.id, username: u.username, displayName: u.displayName, avatar: u.avatar, verified: u.verified }
}
